package kr.or.ddit.dto;

public class CorporationVO {

	private String coName;
	private String coHomeurl;
	private String coLogo;
	private String coNo;
	private String coContent;
	private String coSummary;
	private String coBusino;

	public String getCoName() {
		return coName;
	}

	public void setCoName(String coName) {
		this.coName = coName;
	}

	public String getCoHomeurl() {
		return coHomeurl;
	}

	public void setCoHomeurl(String coHomeurl) {
		this.coHomeurl = coHomeurl;
	}

	public String getCoLogo() {
		return coLogo;
	}

	public void setCoLogo(String coLogo) {
		this.coLogo = coLogo;
	}

	public String getCoNo() {
		return coNo;
	}

	public void setCoNo(String coNo) {
		this.coNo = coNo;
	}

	public String getCoContent() {
		return coContent;
	}

	public void setCoContent(String coContent) {
		this.coContent = coContent;
	}

	public String getCoSummary() {
		return coSummary;
	}

	public void setCoSummary(String coSummary) {
		this.coSummary = coSummary;
	}

	public String getCoBusino() {
		return coBusino;
	}

	public void setCoBusino(String coBusino) {
		this.coBusino = coBusino;
	}

}
